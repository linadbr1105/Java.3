package sec04.exam06_assignment;

public class AssignmentOperatorExample {
  public static void main(String[] args){
    
    int result = 10;
    result += 10;
    System.out.println("result ="+result);
    //result=20

    result -= 5;
    System.out.println("result ="+result);
    //result=15

    result *= 3;
    System.out.println("result="+result);
    //result=45

    result /=5;
    System.out.println("result="+result);
    //result=9

    result %=5;
    System.out.println("result="+result);
    //result=4
 }
}
